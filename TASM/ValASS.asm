TITLE  Programma			
;VALORE ASSOLUTO DI UN NUMERO CONTENUTO IN N
.MODEL SMALL
.STACK
.DATA
N DB ?
.CODE
.STARTUP
MOV AH, 01H
INT 21H			;INPUT IN AL
MOV CL, AL		;CL=AL
CMP CL, '-'
JE MINORE

CMP N, 00H
JB MINORE
JMP FINE

MINORE:		MOV AH, 01H
			INT 21H			;INPUT DEL NUMERO N
			MOV N, AL		;N=AL
			;SE N<0 IL VALORE ASSOLUTO Ã¨ -N
			MOV BL, 00H			;BL=0
			SUB BL, N			;BL = BL-N = 0-N = -N
			MOV DL, BL			;DL=BL
			MOV AH, 02H
			INT 21H				;OUTPUT DA DL
			
FINE: 	MOV AH, 4CH
		INT 21H






END